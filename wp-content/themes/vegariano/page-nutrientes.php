                <h6 class="card-title card-header">Listado De Nutrientes Para Una Alimentación Vegana</h6>
                <br>
                <p class="card-text"> <b>HIDRATOS DE CARBONO</b>
                  <br>
                  <br>
                  Son esenciales en la dieta pues la necesidad más urgente del cuerpo humano es obtener energía y éstos la aportan en un metabolismo normal.
                  <br>
                  Aparte ahorran proteínas. Los HdC son un grupo de sustancias que incluyen los azucares más simples (fructuosa (azúcar de frutas, la + importante), galactosa y glucosa), los disacáridos (lactosa, maltosa y sacarosa) y los polisacáridos (celulosa, glucógeno y almidón) Podemos encontrarlos en: Cereales integrales y sus subproductos: arroz, trigo, avena, fideos; frutas como bananas, uvas y ciruelas; legumbres: arvejas, lentejas y garbanzos; frutas secas como pasas, dátiles e higos; oleaginosas como nueces, almendras, castañas, avellanas y maní; verduras como papas y tapioca
                  <br>
                  <br>
                  <b>GRASAS</b>
                  <br>
                  <br>
                  Forman el tejido adiposo, actúa de recubrimiento de los órganos internos, conservando el calor corporal como una capa bajo la piel. Son fundamentales para recubrir las neuronas (mielina) y el buen funcionamiento del sistema nervioso. También son una fuente concentrada de energías a las que se recurre en segundo lugar, después de los HdC o en específicos esfuerzo físicos. Podemos encontrarlas mayoritariamente en: Algas vegetales, nueces, almendras, avellanas y aceitunas. También en: cereales integrales, paltas, semillas, verduras, legumbres, raíces, etc.
                  <br>
                  <br>
                  <b>VITAMINAS</b>
                  <br>
                  <br>
                  Son fundamentales para la digestión, para la reproducción y el buen funcionamiento del sistema nervioso y muscular, favorecen el crecimiento de los tejidos y la producción de anticuerpos. Si existe deficiencia de alguna de ellas, se produce una enfermedad determinada, la que se revierte al ingerir la vitamina que faltaba.
                  <br>
                  <br>
                  <strong>- Vitamina A:</strong> Esta se presenta como tal solo en los productos animales, pero los carotenas contenidos en los vegetales son transformados por el organismo en Vit. A, esta además se almacena, por lo que si consumimos habitualmente carotenos de vegetales, siempre tendremos reservas de esta vitamina. Las conseguimos en: Vegetales amarillos, y amarillo-rojizos y verdes como son las zanahorias, ajíes, calabazas, tomates, papas, espinaca, diente de león, batata, remolacha, cebolla, ciruela, pasas, lechuga, etc.
                  <br>
                  <br>
                  <strong>- Vitamina C:</strong>
                  Una dieta vegana aporta más vitamina C que una dieta cárnica, por lo que no vale la pena consumir pastillas de esta vit. La hallamos en: cítricos (naranja, kiwi, pomelos, limón, mandarina, etc), tomate, pimientos verdes, verduras de hoja, coliflor, brócoli, papas, repollitos de bruselas, melones y mangos.
                  <br>
                  <br>
                  <strong>- Vitamina D:</strong> Se almacena en el cuerpo, es vital para la absorción del calcio y su utilización, por esto es importante para niños y embarazadas. Se la llama también “vitamina solar”, ya que la mejor fuente es la simple exposición del cuerpo al sol. La sobredosis (en los que suelen tomar pastillas) puede ser peligroso.
                  <br>
                  <br>
                  <strong>- Vitamina E:</strong> Evita la coagulación de la sangre dentro de las venas, actúa contra la vejez y mantiene la fertilidad por más años.La mayoría de los alimentos la poseen, pero abunda en: frutos secos, aceites y cereales integrales.
                  <br>
                  <br>
                  <strong>- Vitamina K:</strong> Efectos antihemorrágicos y de coagulación, es rara su escacez pero es esencial en la dieta. Se encuentra en: vegetales verdes y amarillos, verduras de hoja, repollo y espinacas.
                  <br>
                  <br>
                  <strong>Complejo Vitamina B:</strong> Todas desempeñan un papel importante en el metabolismo de los Hidratos de Carbono, las grasas y proteínas, también ayudan al funcionamiento del sistema nervioso. Las siguientes son las fuentes principales de estas vitaminas:
                  <br>
                  
                  --><b><var>B1:</var></b> levadura de cerveza, germen de trigo y soja
                  <br>
                  --><b><var>B2 </var>:</b> (riboflavina) levadura de cerveza, cereales enriquecidos, almendras
                  <br>
                  --><b><var>B3 </var>:</b> levadura de cerveza, legumbres, mantequilla de maní
                  <br>
                  --><b><var>B6 </var>:</b> levadura de cerveza, cereales integrales, bananas y verduras.
                  <br>
                  --><b><var>B12 </var>:</b> es importante ya que junto al ácido fólico regula la producción de los glóbulos rojos en la médula ósea. Es de difícil obtención para los veganos, ya que se encuentra en los productos animales. Se la puede encontrar en : levadura de cerveza cultivada en medio rico en B12 (leer etiqueta), leche de soja fortificada, sucedáneos de carne (seitan, pasta de soja) siempre que estén enriquecidos en la citada vitamina. Ningún alimento vegetal es una fuente fiable de B12. De hecho, muchos alimentos vegetales como las algas o alimentos fermentados, contienen "análogos" de B12, sustancias parecidas a la B12, pero sin actividad real en nuestro cuerpo, y que además bloquean la acción de la verdadera B12. Existen también muchas pastillas de esta vitamina que son veganas.
                  <br><a href="http://www.hazteveg.com/p/505/veganismo-y-vitamina-b12">En esta página</a> hay más información al respecto.
                  <br>
                  --><b><var>Ácido Fólico </var>:</b> actúa junto a la B12 en la fabricación de glóbulos rojos, actúan uno en ausencia del otro.
                  Se encuentra en: lechugas, legumbres, espinacas, bananas, frutos secos y cereales.
                  <br>
                  --><b><var>Ácido Pantoténico </var>:</b> es importante para la liberación de energía y la síntesis de muchos compuestos corporales.
                  Se encuentra en muchos alimentos por ej.: levadura de cerveza, salvado de trigo, repollo y arvejas.
                  <br>
                  --><b><var>Biotina </var>:</b> ejerce un papel importante en la utilización de los Hidratos de Carbono, las grasas y las proteínas. Se encuentra en la mayoría de los alimentos y además la sintetiza el propio organismo.
                  También en: los porotos verdes, soja, frutos secos y levadura de cerveza.
                  <br>
                  <br>
                  <strong>MINERALES</strong>
                  <br>
                  <br>
                  Son necesarios como reguladores químicos y materiales de construcción.
                  <br>
                  <br>
                  <b><var>Calcio :</var></b> es el mineral més abundante en el cuerpo, el 99% esta en los huesos y dientes. La ingesta excesiva de protéinas, limita la absorción del calcio, como ocurre en la dieta basada en productos animales. <a href="http://www.hazteveg.com/p/937/el-calcio-en-una-alimentacion-vegetariana-estricta">En esta página</a> hay más información al respecto. Se halla en semillas de sésamo, perejil, soja, chauchas, higos, dátiles, verduras de hoja (espinaca, acelga, etc.) y también en jugos fortificados.
                  <br>
                  <b><var>Hierro :</var></b> combinado con una proteína forman la hemoglobina, sustancia de la sangre que transporta el oxígeno de los pulmones a los órganos y tejidos. Se halla en algas, soja y derivados, vegetales de hoja (espinacas mayormente), papas, lentejas, frutas secas, cereales integrales, perejil, chauchas y levadura de cerveza.
                  La presencia de vitamina C favorece la correcta absorción del hierro, por lo que es muy recomendable acompañar por ejemplo: una ensalada de lentejas (hierro) con jugo de limon (vit. C) u otras frutas. Además cocinar con los viejos utensillos de cocina hechos de hierro, puede aumentar la ingestión de este mineral entre un 100 y un 400%, curioso pero útil.
                  <br>
                  <b><var>Zinc :</var></b> es importante en el mantenimiento del sentido del gusto y en el crecimiento y sobre todo en la maduración sexual. El execivo consumo de alcohol provoca un aumento de la eliminación del zinc. Las fuentes son algas, semillas de calabaza, cereales integrales, legumbres, semillas de sésamo, frutas secas, soja y levadura de cerveza.
                  <br>
                  <b><var>Fósforo :</var></b> influye en la formación de huesos y dientes. Permite el almacenamiento y la liberación de energía. La ingestión excesiva de fósforo disminuye la absorcion del calcio, lo que lleva al organismo a tomarlo de los propios huesos (futura osteoporosis). Una dieta basada en carnes y refrescos carbónicos (coca-cola o pepsi, etc.) puede resultar en un desequilibrio calcio-fósforo, por la grandes cantidades presentes de fósforo en estos productos. Fuentes apropiadas son germen de trigo, avena, maníes, arvejas, levadura de cerveza (algunas vienen enriquecidas con calcio para mayor beneficio).
                  <br>
                  <b><var>Sodio/Cloro/Potasio :</var></b> juntos controlan el balance líquido del organismo, las respuestas nerviosas y las contracciones musculares. Los occidentales consumimos entre 3 y 6 veces más sodio del que necesitamos en realidad, el exceso de este, aumenta la retención de líquidos y eleva el peso corporal. Obtenemos sodio y cloro de las algas, la sal de mesa, y las verduras en general, y potasio: de la levadura de cerveza, el salvado, papas, bananas, naranjas y tomates.
                  <br>
                  <b><var>Magnesio :</var></b> liberador de energía, absorbe y transporta nutrientes y transmite impulsos nerviosos y contracciones musculares. Se encuentra en verduras frescas, algas, harina de soja, nueces, almendras, tofu (queso de soja), cereales integrales y semillas de sésamo.
                  <br>
                  <b><var>Yodo :</var></b> es un componente de la hormona tiroidea, que regula funciones metabólicas básicas. Su deficiencia puede provocar bocio.
                  Este problema disminuye al incluir yodo en la sal de mesa (sal yodada). Fuentes en sal yodada algas (ojo, un exceso de algas puede ser perjudicial para el tiroides por exceso de yodo) y en mucha menor medida verduras y legumbres.
                  <br>
                  <br>
                  <strong>PROTEÍNAS</strong>
                  <br>
                  <br>
                  Solo las proteínas contienen nitrógeno, uno de los 4 elementos fundamentales (carbono-hidrógeno-oxígeno-nitrógeno, los tres primeros se encuentran en las grasas y los hidratos de carbono), para el funcionamiento químico de animales y plantas.
                  <br>
                  Las proteínas son las principales formadoras de vida. Derivado del latín “Proteios” que significa “Primero” o “Principal”. Los más notables fenómenos de la vida se producen gracias y a través de ellas, como por ejemplo el crecimiento y el desarrollo.
                  <br>
                  Encontramos proteínas en: Semillas de girasol, tofu, nueces, salvado de trigo, cebada, mijo, arroz, chauchas, coliflor, mantequilla de maní, arvejas, lentejas, frutas oleaginosas, champiñones y pastas.
